map<pair<int,ll>,ll> mp;

ll solve0(int b, ll v){
    if (mp.count(MP(b,v))) return mp[MP(b,v)];
    if (!b) return v==1;
    return mp[MP(b,v)]=solve0(b-1,v)+(v&1?0:solve0(b-1,v>>1));
}

int main(){
    ll n,t;
    cin>>n>>t;
    ++n;
    ll res=0;

    int b=1;
    for (; 1ll<<(b+1)<=n; ++b) res+=solve0(b,t);
    ll rem=n-(1ll<<b)+1;
    for (int u=b; u>=0; --u){
        if (1ll<<u<=rem){
            res+=solve0(u,t), rem-=1ll<<u;
            if (t&1) break;
            t>>=1;
        }
    }

    cout<<res<<endl;
    return 0;
}

