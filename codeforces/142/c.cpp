const int maxn=1e6+10;
vi e[maxn];
vector<pii> el;


int main(){
    int n,m;
    cin>>n>>m;

    REP(i,m){
        int a, b; 
        scanf(" %d%d",&a,&b); --a; --b;
        el.pb(MP(a,b));
        e[a].pb(b);
        e[b].pb(a);
    }

    ll res=1ll*n*(n-1)*(n-2)/6;
    ll cnt1=0, cnt2=0;

    REP(i,m){
        int a=el[i].ST, b=el[i].ND;
        if (e[a].size()>e[b].size()) swap(a,b);
        int na=e[a].size(), nb=e[b].size();
        
        cnt1+=n-2-(na+nb-2);
        cnt2+=na+nb-2;
    }
    res-=cnt1+cnt2/2;
    cout<<res<<endl;
    return 0;
}

