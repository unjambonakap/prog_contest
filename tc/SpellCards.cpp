int dp[maxn][maxn];

int go(int a, int b){
    if (a==n) return 0;
    int &r=dp[a][b];
    if (r!=-1) return r;
    r=go(a+1,b);
    if (tb[a].ST<=b) r=max(r,go(a+1,b-tb[a].ST)+tb[a].ND);
    return r;
}

